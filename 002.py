n1 = input('digite seu nome: ' )
print(f'O tipo primitivo de {n1}é', type(n1))
print(f'é um número?', n1.isnumeric())
print(f'é alfabético?', n1.isalpha())
print(f'é alfanumérico?', n1.isalnum())
print(f'tem apenas espaços?', n1.isspace) 
print(f'É maiuscula?', n1.isupper())
print(f'É minuscula', n1.islower()) #tem letras minusculas ? 
print(f'está capitalizada?', n1.istitle) #tem letra maiscula e minusculas ? 





