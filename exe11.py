t = float(input("1L de tinta pinta quanto da área da parede?: "))
h = float(input('Digite a altura da parede em metros '))
l = float(input('Digite a largura da parede em metros: '))
c = (h * l) 
n = (h * l) / t 
print(f'A área da parede é {c} m quadradros, será necessário{n}L de tintas para pintar a parede')